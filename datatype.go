package main
import "fmt"

func main(){
	var a int = 10
	var b bool = true
	var c float32 = 3.14
	var d float64 = 3.1448573495853495
	var e string = "This is string"

	fmt.Println("Integer: ", a)
	fmt.Println("Boolean: ", b)
	fmt.Println("Float 32bit: ", c)
	fmt.Println("Float 64bit: ", d)
	fmt.Println("String: ", e)
}